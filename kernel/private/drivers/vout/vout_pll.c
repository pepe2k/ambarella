static int ambarella_vout_clk_set_rate(struct clk *c, unsigned long rate)
{
	int ret_val = -1;
	u32 pre_scaler;
	u32 ctrl2;
	u32 ctrl3;
	u64 divident;
	u64 divider;
	u32 middle;
	union ctrl_reg_u ctrl_reg;
	union frac_reg_u frac_reg;
	u64 diff;

	if (c->extra_scaler > 1) {
		rate *= c->extra_scaler;
	}

	if (!rate) {
		ret_val = -1;
		goto ambarella_vout_clk_set_rate_exit;
	}

	pre_scaler = ((REF_CLK_FREQ + 3000000) / 6000000);
	amba_rct_writel(c->pres_reg, pre_scaler);
	pre_scaler = amba_rct_readl(c->pres_reg);

	middle = ambarella_rct_find_pll_table_index(rate, pre_scaler,
		ambarella_rct_vout_table, (sizeof(ambarella_rct_vout_table) /
		sizeof(struct pll_table_s)));

	ctrl_reg.w = amba_rct_readl(c->ctrl_reg);
	ctrl_reg.s.intp = ambarella_rct_vout_table[middle].intp;
	ctrl_reg.s.sdiv = ambarella_rct_vout_table[middle].sdiv;
	ctrl_reg.s.sout = ambarella_rct_vout_table[middle].sout;
	ctrl_reg.s.bypass = 0;
	ctrl_reg.s.frac_mode = 0;
	ctrl_reg.s.force_reset = 0;
	ctrl_reg.s.power_down = 0;
	ctrl_reg.s.halt_vco = 0;
	ctrl_reg.s.tristate = 0;
	ctrl_reg.s.force_lock = 1;
	ctrl_reg.s.force_bypass = 0;
	ctrl_reg.s.write_enable = 0;
	amba_rct_writel(c->ctrl_reg, ctrl_reg.w);
	ctrl_reg.s.write_enable = 1;
	amba_rct_writel(c->ctrl_reg, ctrl_reg.w);
	ctrl_reg.s.write_enable = 0;
	amba_rct_writel(c->ctrl_reg, ctrl_reg.w);

	amba_rct_writel(c->post_reg, ambarella_rct_vout_table[middle].post);

	if (c->frac_mode) {
		c->rate = ambarella_rct_clk_get_rate(c);
		if (c->rate < rate) {
			diff = rate - c->rate;
		} else {
			diff = c->rate - rate;
		}
		vout_dbgv("rate = %lu\n", rate);
		vout_dbgv("c->rate = %llu\n", c->rate);
		vout_dbgv("diff = %llu\n", diff);

		divident = (diff * pre_scaler *
			(ambarella_rct_vout_table[middle].sout + 1) *
			ambarella_rct_vout_table[middle].post);
		divident = divident << 32;
		divider = ((u64)REF_CLK_FREQ *
			(ambarella_rct_vout_table[middle].sdiv + 1));
		AMBCLK_DO_DIV_ROUND(divident, divider);
		if (c->rate <= rate) {
			frac_reg.s.nega	= 0;
			frac_reg.s.frac	= divident;
		} else {
			frac_reg.s.nega	= 1;
			frac_reg.s.frac	= 0x80000000 - divident;
		}
		vout_dbgv("frac_reg.s.nega = 0x%08X\n", frac_reg.s.nega);
		vout_dbgv("frac_reg.s.frac = 0x%08X\n", frac_reg.s.frac);
		amba_rct_writel(c->frac_reg, frac_reg.w);

		ctrl_reg.w = amba_rct_readl(c->ctrl_reg);
		if (diff) {
			ctrl_reg.s.frac_mode = 1;
		} else {
			ctrl_reg.s.frac_mode = 0;
		}
		ctrl_reg.s.force_lock = 1;
		ctrl_reg.s.write_enable = 1;
		amba_rct_writel(c->ctrl_reg, ctrl_reg.w);

		ctrl_reg.s.write_enable	= 0;
		amba_rct_writel(c->ctrl_reg, ctrl_reg.w);
	}
	if (ctrl_reg.s.frac_mode) {
		ctrl2 = 0x3f710000;
		ctrl3 = 0x00069300;
	} else {
		ctrl2 = 0x3f710000;
		ctrl3 = 0x00068300;
	}

	amba_rct_writel(c->ctrl2_reg, ctrl2);
	amba_rct_writel(c->ctrl3_reg, ctrl3);
	ret_val = 0;

ambarella_vout_clk_set_rate_exit:
	c->rate = ambarella_rct_clk_get_rate(c);

	return ret_val;
}

